package basics;

public class ArithmeticOperators {

	public static void main(String[] args) {
		System.out.println(100+131);
		int a=100;
		int b=5000;
		int c=a+b;
		System.out.println(c);
		System.out.println(121*100);
		System.out.println(121-50);
		System.out.println(100/50);//Quotient
		System.out.println(100%50);//remainder
		
	}

}
