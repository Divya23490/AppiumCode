package basics;

public class RelationalClass {

	public static void main(String[] args) {
		System.out.println(121<100);//false
		System.out.println(121>100);//true
		System.out.println(121>=100);//true
		System.out.println(121<=100);//false
		System.out.println(121!=100);//true
		System.out.println(121==100);//false
	}

}
